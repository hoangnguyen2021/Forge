@Immutable
data class ForgeExtendedColors(
    val live: Color,
    val onLive: Color,
    val liveContainer: Color,
    val onLiveContainer: Color,
)

private val DarkExtendedColors = ForgeExtendedColors(
    live = liveDark,
    onLive = onLiveDark,
    liveContainer = liveContainerDark,
    onLiveContainer = onLiveContainerDark,
)

private val LightExtendedColors = ForgeExtendedColors(
    live = liveLight,
    onLive = onLiveLight,
    liveContainer = liveContainerLight,
    onLiveContainer = onLiveContainerLight,
)

/** Access the Forge extended colors from within a [ForgeTheme]. */
val LocalForgeExtendedColors = staticCompositionLocalOf { DarkExtendedColors }

@Composable
fun ForgeTheme(
    darkTheme: Boolean = isSystemInDarkTheme(),
    content: @Composable () -> Unit,
) {
    val colorScheme = if (darkTheme) DarkColors else LightColors
    val extendedColors = if (darkTheme) DarkExtendedColors else LightExtendedColors

    CompositionLocalProvider(LocalForgeExtendedColors provides extendedColors) {
        MaterialTheme(
            colorScheme = colorScheme,
            typography = Typography,
            content = content,
        )
    }
}
